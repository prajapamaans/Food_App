  static List<FoodItem> getByCategory(String category) {
    if (category == 'All') return items;
    return items.where((f) => f.category == category).toList();
  }

  static List<FoodItem> get topRated {
    final sorted = [...items];
    sorted.sort((a, b) => b.rating.compareTo(a.rating));
    return sorted.take(6).toList(); // show 6 in featured now
  }

  // Search by name or description
  static List<FoodItem> search(String query) {
    if (query.isEmpty) return items;
    final q = query.toLowerCase();
    return items.where((f) =>
      f.name.toLowerCase().contains(q) ||
      f.description.toLowerCase().contains(q) ||
      f.category.toLowerCase().contains(q)
    ).toList();
  }

